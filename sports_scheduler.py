# Rules
# 4 teams: 2 in each division
# Each team plays the other in the division twice
# Each team plays one team not in the division once
# So in total 4 weeks
# Teams prefer to play division games later

# Variables: play_ijk = team i plays team j in week k
# Constraints:
# play_ijk is 0 or 1
# play_ij1 + ... + play_ij4 = 2 if i and j are in the same division
# play_ij1 + ... + play_ij4 = 4 if i and j are not in the same division


